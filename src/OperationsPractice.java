
public class OperationsPractice 
{

	public static void main(String[] args) 
	{
		int number;
		number = 3;
		System.out.println("the initial number is: " + number);
		number +=5;
		System.out.println("Add five: " + number);
		number /=2;
		System.out.println("Divide by 2: " + number);
		number *=4;
		System.out.println("Multiply by four: " + number);
		number -=6;
		System.out.println("Subtract 6: " + number);
		number %=2;
		System.out.println("Modularly divide by 2: " + number);
		
		
	}

}
